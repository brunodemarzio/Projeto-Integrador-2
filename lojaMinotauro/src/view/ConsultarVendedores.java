    private void btnPesquisarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnPesquisarActionPerformed
        // TODO add your handling code here:
        String nome = null;
        if(txtNome.getText().trim().equals("")){
            JOptionPane.showMessageDialog(this, "Nome obrigatório!");
        } else {
            nome = txtNome.getText();
        }
        
        ArrayList<Vendedor> listaVendedores = vendedorDAO.Pesquisar();
        
        DefaultTableModel tmVendedores = new DefaultTableModel();
        tmVendedores.addColumn("IdVend");
        tmVendedores.addColumn("nome");
        tmVendedores.addColumn("sexo");
        tmVendedores.addColumn("cpf");
        
        tblVendedores.setModel(tmVendedores);
        
        tblVendedores.removeColumn(tblVendedores.getColumnModel().getColumn(0));
        tmVendedores.setRowCount(0);
        
        
        
        for (Vendedor p : listaVendedores){
            tmVendedores.addRow(new Object[]{p.getidVendedor(),p.getNome(), p.getSexo(), p.getCPF(),});
        }
        
        tblVendedores.getColumnModel().getColumn(0).setPreferredWidth(500); //ID
        tblVendedores.getColumnModel().getColumn(1).setPreferredWidth(300); // Nome
        tblVendedores.getColumnModel().getColumn(2).setPreferredWidth(300); //Sexo
        tblVendedores.getColumnModel().getColumn(3).setPreferredWidth(100); //CPF
        
    }//GEN-LAST:event_btnPesquisarActionPerformed

