    private void btnDeletarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeletarActionPerformed
        // TODO add your handling code here:
        String nome = null;
        if(txtNome.getText().trim().equals("")){
            JOptionPane.showMessageDialog(this, "Nome obrigatório!");
        } else {
            nome = txtNome.getText();
        }
        
        String nomeVendedor = txtNome.getText();
        boolean retorno = vendedorController.Excluir(nomeVendedor);
        
        if (retorno == true){
        JOptionPane.showMessageDialog(this, "Vendedor excluido com sucesso!");
        }
        else{
        JOptionPane.showMessageDialog(this, "Falha na exclusão!");
        }
    }//GEN-LAST:event_btnDeletarActionPerformed

