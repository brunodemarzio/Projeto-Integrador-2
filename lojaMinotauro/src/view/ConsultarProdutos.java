    private void DeletarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_DeletarActionPerformed
        // TODO add your handling code here:
        String nome = null;
        if(txtNome.getText().trim().equals("")){
            JOptionPane.showMessageDialog(this, "Nome obrigatório!");
        } else {
            nome = txtNome.getText();
        }
        
        String nomeProduto = txtNome.getText();
        boolean retorno = produtoController.Excluir(nomeProduto);
        
        if (retorno == true){
        JOptionPane.showMessageDialog(this, "produto foi excluido com sucesso!");
        }
        else{
        JOptionPane.showMessageDialog(this, "falha na exclusão!");
        }
        
    }//GEN-LAST:event_DeletarActionPerformed

