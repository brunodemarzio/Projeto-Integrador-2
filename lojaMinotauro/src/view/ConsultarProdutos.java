    private void PesquisarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_PesquisarActionPerformed
        // TODO add your handling code here:
        
        String nome = null;
        if(txtNome.getText().trim().equals("")){
            JOptionPane.showMessageDialog(this, "Nome obrigatório!");
        } else {
            nome = txtNome.getText();
        }
        
        ArrayList<Produto> listaProdutos = produtoDAO.Pesquisar();
        
        DefaultTableModel tmProdutos = new DefaultTableModel();
        tmProdutos.addColumn("IdProduto");
        tmProdutos.addColumn("nome");
        tmProdutos.addColumn("categoria");
        tmProdutos.addColumn("quantidade");
        tmProdutos.addColumn("preco");
        
        tblProdutos.setModel(tmProdutos);
        
        tblProdutos.removeColumn(tblProdutos.getColumnModel().getColumn(0));
        tmProdutos.setRowCount(0);
        
        
        
        for (Produto p : listaProdutos){
            tmProdutos.addRow(new Object[]{p.getidProduto(),p.getnome(),p.getcategoria(),p.getquantidade(),p.getpreco()});
        }
        
        tblProdutos.getColumnModel().getColumn(0).setPreferredWidth(500); //ID
        tblProdutos.getColumnModel().getColumn(1).setPreferredWidth(300); // Nome
        tblProdutos.getColumnModel().getColumn(2).setPreferredWidth(300); //Categoria
        tblProdutos.getColumnModel().getColumn(3).setPreferredWidth(100); //Quantidade
        tblProdutos.getColumnModel().getColumn(3).setPreferredWidth(100); //Preco
        
    }//GEN-LAST:event_PesquisarActionPerformed

